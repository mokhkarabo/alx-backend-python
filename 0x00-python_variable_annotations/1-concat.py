#!/usr/bin/env python3
"""
This module is for basic annotations- concat
"""


def concat(str1: str, str2: str) -> str:
    """
    Add two strings together.

    Args:
        str1 (string: The first string.
        str2 (string): The second nstring.

    Returns:
        string: The sum of the two strings.
    """
    return str1 + str2
